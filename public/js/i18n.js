// ── Init ──────────────────────────────────────────────────────────
async function _initI18n() {
  const lang = localStorage.getItem("biblog_lang") || "en";
  await i18next.init({ lng: lang, fallbackLng: "en", resources: _resources });
  _applyAll();
}

function _applyAll() {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key  = el.getAttribute("data-i18n");
    const attr = el.getAttribute("data-i18n-attr");
    const val  = i18next.t(key);
    if (attr) el.setAttribute(attr, val);
    else      el.textContent = val;
  });
  document.documentElement.lang = i18next.language;
  // Update lang label in settings if present
  const labels = { en:"🇬🇧 English", tr:"🇹🇷 Türkçe", de:"🇩🇪 Deutsch",
                   fr:"🇫🇷 Français", es:"🇪🇸 Español", ar:"🇸🇦 العربية" };
  const sel = document.getElementById("langSelected");
  if (sel) sel.textContent = labels[i18next.language] || i18next.language;
}

// Public API
window.t = (key, opts) => i18next.t(key, opts);

window.changeLanguage = async function(lang) {
  localStorage.setItem("biblog_lang", lang);
  await i18next.changeLanguage(lang);
  _applyAll();
  // Close dropdown if open
  const dd = document.getElementById("langDropdown");
  if (dd) dd.classList.remove("open");
};

document.addEventListener("DOMContentLoaded", _initI18n);
