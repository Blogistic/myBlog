const languageLabels = { 
    en: "🇬🇧 English", 
    tr: "🇹🇷 Türkçe", 
    de: "🇩🇪 Deutsch", 
    fr: "🇫🇷 Français", 
    es: "🇪🇸 Español", 
    ar: "🇸🇦 العربية" 
};

let currentLang = localStorage.getItem("language") || "en";
let langInitialized = false;

window.t = (key) => {
    const langData = translations[currentLang] || translations.en;
    return langData[key] || translations.en[key] || key;
};

function applyLanguage(lang) {
    if (!translations[lang]) lang = "en";
    currentLang = lang;
    localStorage.setItem("language", lang);
    
    const t = translations[lang];
    
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (t[key] !== undefined) el.textContent = t[key];
    });
    
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.getAttribute("data-i18n-placeholder");
        if (t[key] !== undefined) el.placeholder = t[key];
    });
    
    document.documentElement.lang = lang;
    
    const langSelected = document.getElementById("langSelected");
    if (langSelected) langSelected.textContent = languageLabels[lang] || lang;
    
    const menuLangSelected = document.getElementById("menuLangSelected");
    if (menuLangSelected) menuLangSelected.textContent = languageLabels[lang] || lang;
    
    window.t = (key) => t[key] || translations.en[key] || key;
}

function changeLanguage(lang) {
    const langDropdown = document.getElementById("langDropdown");
    if (langDropdown) langDropdown.classList.remove("open");
    applyLanguage(lang);
    
    try {
        const bc = new BroadcastChannel("biblog_lang");
        bc.postMessage({ lang });
    } catch(e) {}
}

window.changeLanguage = changeLanguage;
window.applyLanguage = applyLanguage;
window.t = window.t;

function initLanguage() {
    if (langInitialized) return;
    langInitialized = true;
    applyLanguage(currentLang);
    
    const langSelect = document.getElementById("langSelect");
    const langDropdown = document.getElementById("langDropdown");
    
    if (langSelect && langDropdown) {
        langSelect.addEventListener("click", (e) => {
            e.stopPropagation();
            langDropdown.classList.toggle("open");
        });
        
        langDropdown.querySelectorAll("li").forEach(li => {
            li.addEventListener("click", (e) => {
                e.stopPropagation();
                const lang = li.dataset.lang;
                if (lang) changeLanguage(lang);
            });
        });
        
        document.addEventListener("click", () => {
            langDropdown.classList.remove("open");
        });
    }
    
    try {
        const bc = new BroadcastChannel("biblog_lang");
        bc.onmessage = (e) => {
            if (e.data?.lang) applyLanguage(e.data.lang);
        };
    } catch(e) {}
}

if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", initLanguage);
} else {
    initLanguage();
}
